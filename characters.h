// Function that takes a character an returns an index to the corect character in the table down below
uint8_t asciiToTable(char c)
{
    uint8_t ret = 0;
    uint8_t c_i = c;
    if(c_i > 31 && c_i < 127)
    {
        ret = c_i - 32;
    }
    return ret;
}

